    private void doFold(Integer largestX, Integer largestY, String s, Map<String, String> map) {
        String dir = s.split(",")[0];
        String val = s.split(",")[1];
        if (dir.equals("x")) {
            doXFold(largestX, largestY, val, map);
        }
        if (dir.equals("y")) {
            doYFold(largestX, largestY, val, map);
        }
    }

    private void doYFold(Integer largestX, Integer largestY, String val, Map<String, String> map) {
        int foldY = Integer.parseInt(val);
        for (int x = 0; x <= largestX; x++) {
            for (int y = 1; y <= largestY; y++) {
                if ((foldY+y > largestY) || (foldY-y < 0)) {
                    break;
                } else {
                    Integer y1 = foldY-y;
                    String p1 = map.get(x+","+y1);
                    Integer y2 = foldY+y;
                    String p2 = map.get(x+","+y2);
                    if (p2 == null) {
                        break;
                    }
                    if (p1.equals("#") || p2.equals("#")) {
                        map.put(x+","+y1, "#");
                    }
                    map.remove(x+","+y2);
                }
            }
            map.remove(x+","+foldY);
        }
//        printMap(largestX, largestY, map);
    }

    private void doXFold(Integer largestX, Integer largestY, String val, Map<String, String> map) {
        int foldX = Integer.parseInt(val);
        for (int y = 0; y <= largestY; y++) {
            for (int x = 1; x <= largestX; x++) {
                if ((foldX+x > largestX) || (foldX-x < 0)) {
                    break;
                } else {
                    Integer x1 = foldX-x;
                    String p1 = map.get(x1+","+y);
                    Integer x2 = foldX+x;
                    String p2 = map.get(x2+","+y);
                    if (p2 == null) {
                        break;
                    }
                    if (p1.equals("#") || p2.equals("#")) {
                        map.put(x1+","+y, "#");
                    }
                    map.remove(x2+","+y);
                }
            }
            map.remove(foldX+","+y);
        }
//        printMap(largestX, largestY, map);
    }

    private Map<Integer, String> getFoldsList(String testInput) {
        Map<Integer, String> map = new HashMap<>();
        Integer count = 1;
        for (String s : testInput.split("\n")) {
            if (s.contains("fold along")) {
                String dir = s.split("fold along ")[1].split("=")[0];
                String val = s.split("fold along ")[1].split("=")[1];
                map.put(count, dir+","+val);
                count++;
            }
        }
        return map;
    }


