    private Map<String, String> setupMap(int largestX, int largestY) {
        Map<String, String> points = new HashMap<>();
        for (int x = 0; x < largestX+1; x++) {
            for (int y = 0; y < largestY+1; y++) {
                String coord= x+","+y;
                points.put(coord, ".");
            }
        }
        return points;
    }

    private int findLargestX(String input) {
        int maxX = 0;
        for (String s : input.split("\n")) {
            if (s.equals("")) {
                break;
            }
            Integer x = Integer.parseInt(s.split(",")[0]);
            if (x > maxX) {
                maxX = x;
            }
        }
        return maxX;
    }

    private int findLargestY(String input) {
        int maxY = 0;
        for (String s : input.split("\n")) {
            if (s.equals("")) {
                break;
            }
            Integer y1 = Integer.parseInt(s.split(",")[1]);
            if (y1 > maxY) {
                maxY = y1;
            }
        }
        return maxY;
    }

