    private void printBoard(Board finished) {
        StringBuilder sb = new StringBuilder();
        for (int i = 1; i < 26; i++) {
            sb.append(finished.getPoints().get(i).getVal());
            if (finished.getPoints().get(i).isMarked()) {
                sb.append("x");
            }
            sb.append("  ");
            if (i%5 == 0) {
                System.out.println(sb.toString());
                sb = new StringBuilder();
            }
        }
        System.out.println("\n");
    }

    private Integer sumUnmarked(Board board) {
        Integer sum = 0;
        for (Map.Entry<Integer, Point> entry : board.getPoints().entrySet()) {
            if (!entry.getValue().isMarked()) {
                sum += Integer.parseInt(entry.getValue().getVal());
            }
        }
        return sum;
    }

    private List<Board> checkBoards(List<Board> boards) {
        List<Board> finished = new ArrayList<>();
        for (Board board : boards) {
            boolean wins = checkRows(board);
            if (!wins) {
                wins = checkCols(board);
            }
            if (wins) {
                finished.add(board);
            }
        }
        return finished;
    }

    private boolean checkCols(Board board) {
        List<Boolean> pointsMarked = new ArrayList<>();
        for (int i = 1; i < 6; i++) {
            pointsMarked.add(board.getPoints().get(i).isMarked());
            pointsMarked.add(board.getPoints().get(i+5).isMarked());
            pointsMarked.add(board.getPoints().get(i+10).isMarked());
            pointsMarked.add(board.getPoints().get(i+15).isMarked());
            pointsMarked.add(board.getPoints().get(i+20).isMarked());

            if (pointsMarked.contains(false)) {
                pointsMarked = new ArrayList<>();
            } else {
                return true;
            }
        }
        return false;
    }

    private boolean checkRows(Board board) {
        List<Boolean> pointsMarked = new ArrayList<>();
        for (int i = 0; i < 5; i++) {
            pointsMarked.add(board.getPoints().get((i*5)+1).isMarked());
            pointsMarked.add(board.getPoints().get((i*5)+2).isMarked());
            pointsMarked.add(board.getPoints().get((i*5)+3).isMarked());
            pointsMarked.add(board.getPoints().get((i*5)+4).isMarked());
            pointsMarked.add(board.getPoints().get((i*5)+5).isMarked());

            if (pointsMarked.contains(false)) {
                pointsMarked = new ArrayList<>();
            } else {
                return true;
            }
        }
        return false;
    }

    private void markPoints(String n, List<Board> boards) {
        boards.forEach(b -> {
            b.getPoints().forEach( (k, p) -> {
                if (p.getVal().equals(n)) {
                    p.setMarked(true);
                }
            });
        });
    }

    private List<Board> setupBoards() {
        List<Board> boards = new ArrayList<>();
        String[] s = boardsInput.split("\n");

        for (int i = 0; i < s.length; i+=6) {
            Board b = new Board();
            int pos = 1;
            for (int j = 0; j < 5; j++) {
                StringBuilder val = new StringBuilder();
                String row = s[i+j]+" ";
                String[] rowArr = row.split("");
                for (String s1 : rowArr) {
                    if (!s1.equals(" ")) {
                        val.append(s1);
                    } else {
                        if (val.length() > 0) {
                            Point p = new Point();
                            p.setVal(val.toString());
                            p.setMarked(false);
                            b.getPoints().put(pos, p);
                            pos++;
                            val = new StringBuilder();
                        }
                    }
                }
            }
            boards.add(b);
        }
        return boards;
    }

