    private List<Long> splitAndReverseInput(String input) {
        List<Long> intList = new ArrayList<>();
        List<String> splitList = Arrays.asList(input.split("\n"));
        Collections.reverse(splitList);
        splitList.forEach(s -> {
            intList.add(Long.valueOf(s));
        });
        return intList;
    }

    private Long findNotSum(String input, Integer sumRange) {
        List<Long> intList = splitAndReverseInput(input);
        int intListCount = 0;
        for (Long integer : intList) {
            boolean sum = false;
            for (int i = intListCount+1; i < intListCount + sumRange + 1; i++) {
                Long one = intList.get(i);
                for (int j = i + 1; j < i + 1 + sumRange ; j++) {
                    Long two = intList.get(j);
                    Long sumInts = one + two;
                    if (sumInts.equals(integer)) {
                        sum = true;
                        break;
                    }
                }
                if (sum) {
                    break;
                }
            }
            if (!sum) {
                return integer;
            }
            intListCount++;
        }
        return null;
    }

    private Long findInvalidSum(String input, Long invalid) {
        List<Long> intList = splitAndReverseInput(input);
        Collections.reverse(intList);
        for (int i = 0; i < intList.size(); i++) {
            List<Long> values = new ArrayList<>() ;
            Long start = intList.get(i);
            values.add(start);
            Long sum = start;
            for (int j = i + 1; j < intList.size(); j++) {
                values.add(intList.get(j));
                sum += intList.get(j);
                if (sum.equals(invalid)) {
                    return findWeakness(values);
                }
                if (sum > invalid) {
                    values.clear();
                    break;
                }
            }
        }
        return null;
    }

    private Long findWeakness(List<Long> values) {
        Collections.sort(values);
        return values.get(0) + values.get(values.size()-1);
    }

