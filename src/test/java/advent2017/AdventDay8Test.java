    private Integer calcChange(Integer originalValue, String direction, Integer change) {
        if (direction.equalsIgnoreCase("inc")) {
            return originalValue + change;
        } else {
            return originalValue - change;
        }
    }

    private boolean checkMovementCondition(String direction, Integer amount, Integer elementValue) {
        if (direction.equalsIgnoreCase(">")) {
            if (elementValue>amount) {
                return true;
            }
        }
        if (direction.equalsIgnoreCase("<")) {
            if (elementValue<amount) {
                return true;
            }
        }
        if (direction.equalsIgnoreCase(">=")) {
            if (elementValue>=amount) {
                return true;
            }
        }
        if (direction.equalsIgnoreCase("<=")) {
            if (elementValue<=amount) {
                return true;
            }
        }
        if (direction.equalsIgnoreCase("==")) {
            if (elementValue.equals(amount)) {
                return true;
            }
        }
        if (direction.equalsIgnoreCase("!=")) {
            if (!elementValue.equals(amount)) {
                return true;
            }
        }
        return false;
    }

